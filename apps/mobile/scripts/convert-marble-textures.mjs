#!/usr/bin/env node
/**
 * convert-marble-textures.mjs
 *
 * Resizes 2K marble JPEGs → 512x512 RGBA, encodes as base64,
 * writes to apps/mobile/utils/marble-texture-data.ts
 *
 * Usage: npx sharp && node apps/mobile/scripts/convert-marble-textures.mjs
 * Requires: sharp (npm i -D sharp)
 */
import sharp from "sharp";
import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ASSET_DIR = resolve(
  process.env.HOME,
  "Downloads/assets/ziarat_white_marble_tgzkdehv_2k"
);
const OUT_FILE = resolve(__dirname, "../utils/marble-texture-data.ts");
const SIZE = 512;

async function processTexture(filename) {
  const path = resolve(ASSET_DIR, filename);
  console.log(`Processing ${filename} → ${SIZE}x${SIZE} RGBA...`);
  const buf = await sharp(path)
    .resize(SIZE, SIZE, { fit: "cover" })
    .ensureAlpha()
    .raw()
    .toBuffer();
  console.log(`  → ${buf.length} bytes (expected ${SIZE * SIZE * 4})`);
  return buf.toString("base64");
}

async function main() {
  const baseColorB64 = await processTexture(
    "Ziarat_White_Marble_tgzkdehv_2K_BaseColor.jpg"
  );
  const normalB64 = await processTexture(
    "Ziarat_White_Marble_tgzkdehv_2K_Normal.jpg"
  );

  const ts = `// AUTO-GENERATED by convert-marble-textures.mjs — do not edit
export const MARBLE_TEX_SIZE = ${SIZE};
export const MARBLE_BASECOLOR_BASE64 = "${baseColorB64}";
export const MARBLE_NORMAL_BASE64 = "${normalB64}";
`;

  writeFileSync(OUT_FILE, ts);
  console.log(`\nWrote ${OUT_FILE}`);
  console.log(`  BaseColor base64: ${baseColorB64.length} chars`);
  console.log(`  Normal base64: ${normalB64.length} chars`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
